package Arrays;

public class Array {
    public static void main(String[] args) {
        int nums [] = new int [5];
        nums[0] = 1;
        nums[1] = 2;
        nums[2] = 3;
        nums[3] = 4;
        nums[4] = 5;

        for(int i=0; i<5; i++){
            System.out.println(nums[i]);
        }
    }
}